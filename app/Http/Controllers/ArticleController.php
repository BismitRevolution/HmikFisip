     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $article = DB::table('articles')
                    ->where('articles.article_id', '=', $id)
                    ->first();

        return view('articles.show')->with('article', $article);
    }

